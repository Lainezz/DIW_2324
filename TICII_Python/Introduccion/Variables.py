# Variables en PYTHON
# Para declarar una variable en Python debemos hacerlo de la siguiente forma:
# nombre_variable = valor de la variable
# Ejemplos:
"""

edad = 10
nombre = "Pepe"

Lo que se ha hecho en las líneas anteriores es declarar 2 variables,
una de tipo numérico y otra de tipo cadena de caracteres

El valor de esas dos variables se le asigna con el operador =
El valor de edad es 10 y el valor de nombre es "Pepe"

"""

edad = 33 #Edad es numerico
print("La edad de Diego es "+str(edad))

edad = "treinta y tres" #Tipo de dato: caracter
